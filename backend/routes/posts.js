// @route   POST /api/posts/:postId/like
// @desc    Like/unlike post
// @access  Private
router.post("/:postId/like", protect, async (req, res) => {
  try {
    const post = await Post.findById(req.params.postId);

    if (!post) {
      return res.status(404).json({
        success: false,
        error: {
          code: "NOT_FOUND",
          message: "Post not found",
        },
      });
    }

    const likeIndex = post.engagement.likes.indexOf(req.user._id);

    if (likeIndex > -1) {
      // Unlike
      post.engagement.likes.splice(likeIndex, 1);
      post.metadata.likeCount -= 1;
    } else {
      // Like
      post.engagement.likes.push(req.user._id);
      post.metadata.likeCount += 1;
    }

    await post.save();

    // Invalidate cache
    await cache.del(`post:${req.params.postId}`);

    res.json({
      success: true,
      data: {
        liked: likeIndex === -1,
        likeCount: post.metadata.likeCount,
      },
      message: likeIndex === -1 ? "Post liked" : "Post unliked",
    });
  } catch (error) {
    console.error("Like Post Error:", error);
    res.status(500).json({
      success: false,
      error: {
        code: "SERVER_ERROR",
        message: "Error liking post",
      },
    });
  }
});

